    private boolean validInputText(String s) {
        boolean hasDecimal = false;
        boolean valid = true;
        for (int i = 0; i < s.length(); i++)
        {
            char c = s.charAt(i);
            if (c >= '0' && c <= '9')
            {
                continue;
            }
            else if (c == '.' && !hasDecimal)
            {
                hasDecimal = true;
            }
            else
            {
                // invalid character found
                valid = false;
            }
        }
        return valid;
    }

