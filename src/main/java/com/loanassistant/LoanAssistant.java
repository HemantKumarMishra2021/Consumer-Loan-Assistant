    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String loanBalanceStr = jTextField1.getText();
        if(loanBalanceStr.isEmpty() || !validInputText(loanBalanceStr)) {
            JOptionPane.showConfirmDialog(null, "Invalid or empty Loan Balance entry.\nPlease correct.", "Balance Input Error", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        double loanBalance = Double.valueOf(loanBalanceStr).doubleValue();
        System.out.println(loanBalance);
        
        String interestRateStr = jTextField2.getText();
        if(interestRateStr.isEmpty() || !validInputText(interestRateStr)) {
            JOptionPane.showConfirmDialog(null, "Invalid or empty Interest Rate entry.\nPlease correct.", "Interest Input Error", JOptionPane.DEFAULT_OPTION,JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        double interestRate = Double.valueOf(interestRateStr).doubleValue() / 1200;
        
        String noOfPaymentStr = jTextField3.getText();
        
        String monthstr = jTextField4.getText();
        
        if(noOfPaymentStr.isEmpty() && monthstr.isEmpty()) {
            JOptionPane.showConfirmDialog(null, "Either Number of Payments or Monthly Payment empty.\nPlease enter one value.", "Number of Payments or Monthly Payment Input Error", JOptionPane.DEFAULT_OPTION,JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        
        if (noOfPaymentStr.isEmpty())
        {
            if(!validInputText(monthstr)){
                JOptionPane.showConfirmDialog(null, "Invalid Monthly Payment entry.\nPlease correct.", " Monthly Payment Input Error", JOptionPane.DEFAULT_OPTION,JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            double month = Double.valueOf(monthstr);
            int months = (int)((Math.log(month) - Math.log(month - loanBalance * interestRate)) / Math.log(1 + interestRate));
            jTextField3.setText(months +"");
        }
        else if(monthstr.isEmpty())
        {
            if(!validInputText(noOfPaymentStr)) {
                JOptionPane.showConfirmDialog(null, "Invalid Number of Payments entry.\nPlease correct.", "Number of Payments Input Error", JOptionPane.DEFAULT_OPTION,JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            int noOfPayment = Integer.valueOf(noOfPaymentStr);
            double multiplier = Math.pow(1+interestRate, noOfPayment);
            double payment = (loanBalance * interestRate * multiplier) / (multiplier - 1);
            jTextField4.setText(new DecimalFormat("0.00").format(payment));
        }
        
        noOfPaymentStr = jTextField3.getText();
        monthstr = jTextField4.getText();
        
        String message  = "Loan Balance: "+loanBalanceStr;
        
        message += "\nInterest Rate: "+interestRateStr;
        
        message += "\n\n" + "Total number of month: "+noOfPaymentStr;
        
        message +=  "\n" + "Each month EMI: "+monthstr;
        
        jTextArea1.setText(message);
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

